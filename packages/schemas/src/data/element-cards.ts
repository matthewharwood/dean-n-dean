export const PeriodicTablePositionSchema = z.object({
  row: z.int().min(1).max(9),
  column: z.int().min(1).max(18),
  series: PeriodicTableSeriesSchema,
});
export type PeriodicTablePosition = z.infer<typeof PeriodicTablePositionSchema>;

export const ElementMetadataSchema = z.object({
  atomicNumber: z.int().min(1).max(118),
  symbol: ElementSymbolSchema,
  name: z.string().min(1),
  atomicMass: z.string().min(1),
  cpkHexColor: HexColorSchema.nullable(),
  electronConfiguration: z.string().min(1),
  electronegativity: z.number().nullable(),
  atomicRadiusPm: z.number().nullable(),
  ionizationEnergyEv: z.number().nullable(),
  electronAffinityEv: z.number().nullable(),
  oxidationStates: z.array(z.string()),
  standardState: StandardStateSchema,
  meltingPointK: z.number().nullable(),
  boilingPointK: z.number().nullable(),
  densityGPerCm3: z.number().nullable(),
  groupBlock: ElementGroupBlockSchema,
  yearDiscovered: z.string().min(1),
  period: z.int().min(1).max(7),
  group: z.int().min(1).max(18).nullable(),
  block: ElementBlockSchema,
  table: PeriodicTablePositionSchema,
});
export type ElementMetadata = z.infer<typeof ElementMetadataSchema>;

export const ElementCardVisualSchema = z.object({
  accentColor: HexColorSchema,
  familyColor: HexColorSchema,
});
export type ElementCardVisual = z.infer<typeof ElementCardVisualSchema>;

export const ElementCardGameMetadataSchema = z.object({
  complexityTier: ElementComplexityTierSchema,
  rarity: ElementCardRaritySchema,
  starter: z.boolean(),
  tags: z.array(z.string().min(1)),
});
export type ElementCardGameMetadata = z.infer<typeof ElementCardGameMetadataSchema>;

export const AlchemyCardTypeSchema = z.enum(["element"]);
export type AlchemyCardType = z.infer<typeof AlchemyCardTypeSchema>;

export const ElementAlchemyCardSchema = z.object({
  id: z.string().regex(/^element:[a-z]{1,3}$/),
  type: z.literal("element"),
  name: z.string().min(1),
  symbol: ElementSymbolSchema,
  unlockLevel: z.int().min(1),
  visual: ElementCardVisualSchema,
  element: ElementMetadataSchema,
  game: ElementCardGameMetadataSchema,
});
export type ElementAlchemyCard = z.infer<typeof ElementAlchemyCardSchema>;

// This is intentionally an alias today. Widen it to a discriminated union when
// recipes, tools, quests, or upgrade cards join the alchemy deck.
export const AlchemyCardSchema = ElementAlchemyCardSchema;
export type AlchemyCard = ElementAlchemyCard;

type DeepReadonly<T> = T extends readonly (infer U)[]
  ? readonly DeepReadonly<U>[]
  : T extends object
    ? { readonly [K in keyof T]: DeepReadonly<T[K]> }
    : T;

